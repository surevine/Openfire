package org.jivesoftware.openfire.mix;

public class MixPersistenceException extends Exception {

	private static final long serialVersionUID = 1L;

	public MixPersistenceException() {
		super();
	}

	public MixPersistenceException(String message, Throwable cause) {
		super(message, cause);
	}

	public MixPersistenceException(String message) {
		super(message);
	}

	public MixPersistenceException(Throwable cause) {
		super(cause);
	}
	
}
